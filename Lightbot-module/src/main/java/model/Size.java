package model;

public class Size {
	private int high;
	private int widht;

	public Size(int high, int widht) {
		super();
		this.high = high;
		this.widht = widht;
	}

	public int getHigh() {
		return high;
	}

	public void setHigh(int high) {
		this.high = high;
	}

	public int getWidht() {
		return widht;
	}

	public void setWidht(int widht) {
		this.widht = widht;
	}
}
